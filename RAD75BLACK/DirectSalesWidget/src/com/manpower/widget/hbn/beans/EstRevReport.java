package com.manpower.widget.hbn.beans;

public class EstRevReport {
	private long opp_id;
	private String sales_stage;
	private long usd_revenue;
	private long euro_revenue;
	
	public long getOpp_id() {
		return opp_id;
	}
	public void setOpp_id(long opp_id) {
		this.opp_id = opp_id;
	}
	public String getSales_stage() {
		return sales_stage;
	}
	public void setSales_stage(String sales_stage) {
		this.sales_stage = sales_stage;
	}
	public long getUsd_revenue() {
		return usd_revenue;
	}
	public void setUsd_revenue(long usd_revenue) {
		this.usd_revenue = usd_revenue;
	}
	public long getEuro_revenue() {
		return euro_revenue;
	}
	public void setEuro_revenue(long euro_revenue) {
		this.euro_revenue = euro_revenue;
	}
	
	
}
